def bio(name, age):
    def greeting():
        return random.choice(
            [
                f"Привет! Я {name}.",
                "Прветствую.",
                f"Мне {age}",
                "Доброе утро.",
                "АААААААА",
                "Добавь меня в Telegram.",
                "Напиши мне.",
                "Познокомимся?",
                "Здравствуйте.",
                "Салют."
            ]
        )

    def fhobbies():
        s = random.choice(
            [
                "Мне нравиться",
                "Люблю заниматься",
                "Обажаю",
                "Увлекаюсь",
                "Не могу устоять перед",
                "Ненавижу",
                "Хочу",
            ]
        )
        for i in range(random.randint(0, 4)):
            s += " " + random.choice(hobbies) + ","
        return s + " " + random.choice(hobbies) + "."

    def preference():
        s = random.choice(
            [
                "Ищу парня который",
                "Нужен человек обладающий следующими качествами",
                "Перед тем как мне писать убедись что ты",
                "Надеюсь ты хотя бы",
            ]
        )
        for i in range(random.randint(0, 4)):
            s += " " + random.choice(adjectives) + ","
        return s + " " + random.choice(adjectives) + "."

    s = ""
    for i in range(random.randint(1, 2)):
        s += greeting() + " "
    for i in range(1, 4):
        s += random.choice([fhobbies, preference])() + " "
    return s


